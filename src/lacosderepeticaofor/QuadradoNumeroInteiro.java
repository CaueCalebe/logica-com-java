package lacosderepeticaofor;

public class QuadradoNumeroInteiro {
    public static void main(String[] args) {
        
        System.out.println("---- Tabela Quadrado Perfeito do 15 até 200 ----\n");
        
        for (int i = 15; i <= 200; i++) {
            
            System.out.println("Quadrado Perfeito de " + i + " é: " + (i * i));
        }
        
        System.out.println("\nFim do Programa!");
    }
}
